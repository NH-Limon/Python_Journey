# The bool() class:

a = bool() 
print(a) # False
b = bool(0)
print(b) # False
c = bool(2)
print(c) # True
d = bool("")
print(d) # False
e = bool("Limon")
print(e) # True
f = bool([])
print(f) # False
g = bool([1,2,3,4,5])
print(g) # True




